export function EmergencyProvider({ children }: { children: React.ReactNode }) {
  const [cases, setCases] = useState<EmergencyCase[]>(initialCases)
  const [activeCaseId, setActiveCaseId] = useState(1001)
  const [ambulances, setAmbulances] = useState<AmbulanceUnit[]>(initialAmbulances)
  const [hospitals, setHospitals] = useState<HospitalUnit[]>(initialHospitals)
  const [alerts, setAlerts] = useState<SystemAlert[]>([
    { id: 'a1', title: 'O- Blood Low', detail: 'Only 3 units remaining', priority: 'Critical', createdAt: Date.now() - 10000 },
    { id: 'a2', title: 'Hospital A Capacity', detail: '85% bed occupancy', priority: 'High', createdAt: Date.now() - 8000 },
  ])
  const [bloodUnits, setBloodUnits] = useState({ oNegative: 3, abNegative: 4 })
  const [aiLoading, setAiLoading] = useState(false)

  const addAlert = (title: string, detail: string, priority: AlertPriority) => {
    setAlerts((prev) =>
      [{ id: `${Date.now()}-${Math.random()}`, title, detail, priority, createdAt: Date.now() }, ...prev]
        .sort((a, b) => {
          const rank = { Critical: 3, High: 2, Medium: 1 }
          return rank[b.priority] - rank[a.priority] || b.createdAt - a.createdAt
        })
        .slice(0, 8)
    )
  }

  const getBestAcceptingHospital = (fallbackName: string) => {
    const accepting = hospitals.filter((h) => h.acceptsIncoming)
    if (accepting.length === 0) return fallbackName
    const preferred = accepting.find((h) => h.name === fallbackName)
    if (preferred) return preferred.name
    return [...accepting].sort((a, b) => a.capacity - b.capacity)[0].name
  }

  const runAITriage = (caseId = activeCaseId) => {
    setAiLoading(true)
    setTimeout(() => {
      setCases((prev) =>
        prev.map((c) => {
          if (c.id !== caseId) return c
          const severeShift = Math.max(1, Math.round(c.victims * 0.25))
          const moderateShift = Math.max(1, Math.round(c.victims * 0.35))
          const burnShift = Math.max(1, Math.round(c.victims * 0.15))
          const minorShift = Math.max(1, c.victims - severeShift - moderateShift - burnShift)
          return {
            ...c,
            severity: { Critical: severeShift, Burn: burnShift, Moderate: moderateShift, Minor: minorShift },
            suggestedHospitals: { ...roleDefaults },
            timeline: [
              ...c.timeline,
              {
                time: new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' }),
                event: 'AI triage run: severity, role assignment, and hospital allocation updated',
              },
            ],
          }
        })
      )
      setAmbulances((prev) =>
        prev.map((unit, index) => {
          const roles: SeverityRole[] = ['Critical', 'Burn', 'Moderate', 'Minor']
          const role = roles[index % roles.length]
          return {
            ...unit,
            role,
            assignedHospital: getBestAcceptingHospital(roleDefaults[role]),
            status: 'En Route',
            etaMin: Math.max(2, (index % 6) + 2),
          }
        })
      )
      addAlert('AI Triage Completed', `Case #${caseId} synchronized across triage, ambulance, and hospital modules`, 'High')
      setAiLoading(false)
    }, 1200)
  }

  const createCase = (payload: { location: string; caseType: string; estimatedVictims: number; description: string }) => {
    const newId = Math.max(...cases.map((c) => c.id)) + 1
    const nextCase: EmergencyCase = {
      id: newId,
      type: payload.caseType || 'Emergency',
      location: payload.location || 'Unknown location',
      status: 'Active',
      victims: Math.max(1, payload.estimatedVictims),
      timeAgo: 0,
      severity: { Critical: 1, Burn: 1, Moderate: 2, Minor: 1 },
      suggestedHospitals: { ...roleDefaults },
      timeline: [
        { time: new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' }), event: 'Case created by command center operator' },
        { time: new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' }), event: payload.description || 'No description provided' },
      ],
    }
    setCases((prev) => [nextCase, ...prev])
    setActiveCaseId(newId)
    addAlert('New Emergency Case', `Case #${newId} created at ${nextCase.location}`, 'High')
    runAITriage(newId)
  }

  const reassignAmbulance = (ambulanceId: string, role: SeverityRole, hospital: string) => {
    const assignedHospital = getBestAcceptingHospital(hospital)
    setAmbulances((prev) =>
      prev.map((unit) =>
        unit.id === ambulanceId ? { ...unit, role, assignedHospital, status: 'En Route', etaMin: Math.max(2, unit.etaMin || 4) } : unit
      )
    )
  }

  const redirectAmbulances = () => {
    const targetHospital = getBestAcceptingHospital('Hospital B')
    setAmbulances((prev) =>
      prev.map((unit) => (unit.role === 'Moderate' ? { ...unit, assignedHospital: targetHospital, etaMin: unit.etaMin + 2 } : unit))
    )
    addAlert('Ambulances Redirected', `Moderate severity units rerouted to ${targetHospital}`, 'High')
  }

  const requestBloodUnits = () => {
    setBloodUnits((prev) => ({ oNegative: prev.oNegative + 20, abNegative: prev.abNegative + 15 }))
    addAlert('Blood Units Requested', 'Emergency replenishment request sent for O- and AB-', 'Critical')
  }

  const toggleHospitalIntake = (hospitalName: string) => {
    setHospitals((prev) =>
      prev.map((hospital) =>
        hospital.name === hospitalName ? { ...hospital, acceptsIncoming: !hospital.acceptsIncoming } : hospital
      )
    )
    const current = hospitals.find((h) => h.name === hospitalName)
    const nextState = current ? !current.acceptsIncoming : false
    addAlert(
      'Hospital Intake Updated',
      `${hospitalName} is now ${nextState ? 'accepting incoming cases' : 'diverting incoming cases'}`,
      'Medium'
    )
  }

  const applyRecommendation = () => {
    redirectAmbulances()
    requestBloodUnits()
    addAlert('Recommendation Applied', 'AI recommendation package executed successfully', 'High')
  }

  useEffect(() => {
    const timer = setInterval(() => {
      setAmbulances((prev) =>
        prev.map((unit) => {
          if (unit.status !== 'En Route') return unit
          const hospital = initialHospitals.find((h) => h.name === unit.assignedHospital)
          if (!hospital) return unit
          const dx = hospital.x - unit.x
          const dy = hospital.y - unit.y
          const nx = Math.abs(dx) < 0.8 ? hospital.x : unit.x + Math.sign(dx) * 0.8
          const ny = Math.abs(dy) < 0.8 ? hospital.y : unit.y + Math.sign(dy) * 0.8
          const reached = Math.abs(dx) < 1 && Math.abs(dy) < 1
          return {
            ...unit,
            x: nx,
            y: ny,
            etaMin: reached ? 0 : Math.max(1, unit.etaMin - 1),
            status: reached ? 'Busy' : unit.status,
          }
        })
      )
      setHospitals((prev) =>
        prev.map((h) => {
          const delta = Math.random() > 0.5 ? 1 : -1
          const capacity = Math.max(20, Math.min(97, h.capacity + delta))
          const status: HospitalUnit['status'] = capacity > 88 ? 'Red' : capacity > 70 ? 'Yellow' : 'Green'
          return { ...h, capacity, status }
        })
      )
      setCases((prev) => prev.map((c) => ({ ...c, timeAgo: c.timeAgo + 1 })))
    }, 3000)
    return () => clearInterval(timer)
  }, [])

  const value = useMemo(
    () => ({
      cases,
      ambulances,
      hospitals,
      alerts,
      activeCaseId,
      bloodUnits,
      aiLoading,
      createCase,
      setActiveCase: setActiveCaseId,
      runAITriage,
      reassignAmbulance,
      applyRecommendation,
      redirectAmbulances,
      requestBloodUnits,
      toggleHospitalIntake,
    }),
    [cases, ambulances, hospitals, alerts, activeCaseId, bloodUnits, aiLoading]
  )

  return <EmergencyContext.Provider value={value}>{children}</EmergencyContext.Provider>
}

export function useEmergency() {
  const ctx = useContext(EmergencyContext)
  if (!ctx) throw new Error('useEmergency must be used within EmergencyProvider')
  return ctx
}
